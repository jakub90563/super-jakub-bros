info.onLifeZero(function () {
    for (let index = 0; index <= HIGH_SCORES_NAMES.length - 1; index++) {
        if (info.score() > HIGH_SCORES[index]) {
            HIGH_SCORES_NAMES.insertAt(index, game.askForString("enter your name"))
            HIGH_SCORES.insertAt(index, info.score())
            HIGH_SCORES_NAMES.pop()
            HIGH_SCORES.pop()
            break;
        }
    }
    HIGH_SCORE_MESSAGE = "High Scores\\n"
    for (let index2 = 0; index2 <= HIGH_SCORES_NAMES.length - 1; index2++) {
        HIGH_SCORE_MESSAGE = "" + HIGH_SCORE_MESSAGE + HIGH_SCORES_NAMES[index2] + ":" + HIGH_SCORES[index2] + "\\n"
    }
    game.showLongText(HIGH_SCORE_MESSAGE, DialogLayout.Center)
    game.gameOver(false)
})
